
friends = 5

#ADDITION
friends = friends + 1
print(friends)
#auguemented assignment operator
friends += 1
print(friends)

#SUBTRACTION
friends = friends -2
print(friends)
#auguemented assignment operator
friends -= 2
print(friends)

#MULTIPLICATION
friends = friends * 3
print(friends)
#auguemented assignment operator
friends *= 3
print(friends)

#DEVISION
friends = friends / 2
print(friends)
#auguemented assignment operator
friends /= 2
print(friends)

#EXPONENT
friends = friends ** 2
print(friends)

#auguemented assignment operator
friends **=2    
print(friends)  

#REMAINDER - Modulus operator
remainder = friends % 3
print(remainder)    

